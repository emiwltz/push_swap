#include "libft.h"
#include "push_swap.h"


int	is_digit_string(char *s)
{
  int	i;

  i = 0;
  while (s[i])
  {
	if(ft_isdigit(s[i]) == 0 && s[i] != '-')
	  return (0);
	i++;
  }
  return (1);
}
